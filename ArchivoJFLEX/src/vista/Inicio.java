    //Metodo ejecutar desde interfaz
    public void ejecutar() {
        AnalizadorSintactico.result="";
        AnalizadorSintactico.fail="";
        AnalizadorLexico.failLexico="";
        //Llamamos al metodo guardarArchivo y le enviamos la entrada.txt
        guardarArchivo("entrada.txt");
        String[] archivoPrueba = {"entrada.txt"};
        //Enviamos la cadena con las instrucciones al analizador
        AnalizadorSintactico.main(archivoPrueba);
        //Presentamos resultados y errores
        lblResultado.setText(AnalizadorSintactico.result);
        txtArea.setText(AnalizadorSintactico.fail);
        txtArea.setText(txtArea.getText() + " " + AnalizadorLexico.failLexico);
        
    }
    //Metodo que guarda el archivo de texto
    public void guardarArchivo(String direccion) {
        try {
            FileWriter writer = new FileWriter(direccion);
            PrintWriter print = new PrintWriter(writer);
            //Se obtine el texto para agregarlo al archivo que vamos a escribir
            print.print(txtNumero.getText());
            writer.close();
        } catch (IOException e) {
            System.out.println(e.getMessage());
        }
        //fin
    }

